-- ============================================================================
-- STEP 1: BACKUP CURRENT DATA
-- Run these SELECT statements first and save the output.
-- ============================================================================

SELECT *
FROM encounter
WHERE visit_id = 196629;

SELECT *
FROM visit
WHERE visit_id = 196629;

-- ============================================================================
-- STEP 2: UPDATE ENCOUNTER
-- ============================================================================

UPDATE encounter
SET encounter_datetime = '2015-06-24 00:00:00'
WHERE visit_id = 196629
  AND encounter_datetime = '1019-10-30 00:00:00';

-- ============================================================================
-- STEP 3: UPDATE VISIT
-- ============================================================================

UPDATE visit
SET date_started = '2015-06-24 00:00:00',
    date_stopped = '2015-06-24 23:59:59'
WHERE visit_id = 196629;

-- ============================================================================
-- STEP 4: VERIFY THE UPDATE
-- ============================================================================

SELECT *
FROM encounter
WHERE visit_id = 196629;

